function renderDetailsList(details = {}) {
  const rows = Object.entries(details)
    .filter(([, value]) => value)
    .map(([term, value]) => `<div><dt>${escapeHtml(term)}</dt><dd>${escapeHtml(value)}</dd></div>`)
    .join("\n          ");

  return rows ? `<section class="project-copy-block project-meta" aria-label="Project info">
        <h2>Project info</h2>
        <dl class="details-list">
          ${rows}
        </dl>
      </section>` : "";
}

function renderConcept(project = {}) {
  const paragraphs = Array.isArray(project.concept) ? project.concept.filter(Boolean) : [];

  if (!paragraphs.length) {
    return "";
  }

  return `<section class="project-copy-block">
        <h2>Concept</h2>
        ${paragraphs.map((paragraph) => `<p>${escapeHtml(paragraph)}</p>`).join("\n        ")}
      </section>`;
}

function renderProjectStaticHtml(project = {}) {
  const summary = project.summary || cleanDescription(project);
  const descriptionHtml = project.description && project.description !== summary
    ? `<p>${escapeHtml(project.description)}</p>`
    : "";
  const details = renderDetailsList(project.details);
  const concept = renderConcept(project);
  const descriptionBlock = descriptionHtml ? `\n        ${descriptionHtml}` : "";

  return `<section class="project-intro">
    <div class="project-intro-layout">
      <div class="project-copy-main">
        <div class="project-intro-header">
          <p class="eyebrow">${escapeHtml(project.category || project.cardCategory || "Project")}</p>
          <h1>${escapeHtml(project.title || "Project")}</h1>
          <p class="project-summary">${escapeHtml(summary)}</p>
        </div>${descriptionBlock}
      </div>
      <aside class="project-copy-side">
        ${details}
      </aside>
    </div>
    ${concept}
  </section>`;
}


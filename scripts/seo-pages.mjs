function createSitemapXml(projects = []) {
  const publicProjects = projects.filter((project) => project?.slug && !project.hidden);
  const today = new Date().toISOString().slice(0, 10);
  const urls = [
    { loc: `${siteOrigin}/`, priority: "1.0" },
    { loc: `${siteOrigin}/about.html`, priority: "0.7" },
    ...publicProjects.map((project) => ({
      loc: getProjectPageUrl(project),
      priority: "0.8"
    }))
  ];

  return `<?xml version="1.0" encoding="UTF-8"?>
<urlset xmlns="http://www.sitemaps.org/schemas/sitemap/0.9">
${urls.map((item) => `  <url>
    <loc>${escapeHtml(item.loc)}</loc>
    <lastmod>${today}</lastmod>
    <priority>${item.priority}</priority>
  </url>`).join("\n")}
</urlset>
`;
}

async function pruneStaleGeneratedPages(projectsDir, expectedFiles) {
  let entries = [];

  try {
    entries = await readdir(projectsDir, { withFileTypes: true });
  } catch (error) {
    if (error.code === "ENOENT") {
      return;
    }

    throw error;
  }

  await Promise.all(entries
    .filter((entry) => entry.isFile() && entry.name.endsWith(".html") && !expectedFiles.has(entry.name))
    .map(async (entry) => {
      const filePath = join(projectsDir, entry.name);
      const contents = await readFile(filePath, "utf8");

      if (contents.includes(generatedMarker)) {
        await rm(filePath, { force: true });
      }
    }));
}

export async function generateProjectSeoPages(projects = [], rootDir = process.cwd()) {
  const publicProjects = projects.filter((project) => project?.slug && !project.hidden);
  const projectsDir = resolve(rootDir, "projects");
  const expectedFiles = new Set(publicProjects.map((project) => `${project.slug}.html`));

  await mkdir(projectsDir, { recursive: true });
  await pruneStaleGeneratedPages(projectsDir, expectedFiles);

  await Promise.all(publicProjects.map((project) => writeFile(
    join(projectsDir, `${project.slug}.html`),
    createProjectSeoHtml(project)
  )));
  await writeFile(resolve(rootDir, "sitemap.xml"), createSitemapXml(projects));

  return publicProjects.length;
}

async function runFromCli() {
  const scriptDir = dirname(fileURLToPath(import.meta.url));
  const rootDir = resolve(scriptDir, "..");
  const projectsFile = await readFile(resolve(rootDir, "data", "projects.js"), "utf8");
  const projectsJson = projectsFile
    .replace(/^window\.PORTFOLIO_PROJECTS\s*=\s*/, "")
    .replace(/;\s*$/, "");
  const projects = JSON.parse(projectsJson);
  const count = await generateProjectSeoPages(projects, rootDir);
  console.log(`Generated ${count} project SEO page${count === 1 ? "" : "s"}.`);
}

if (process.argv[1] === fileURLToPath(import.meta.url)) {
  runFromCli().catch((error) => {
    console.error(error);
    process.exitCode = 1;
  });
}

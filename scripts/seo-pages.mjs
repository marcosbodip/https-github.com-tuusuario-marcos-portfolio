import { mkdir, readdir, readFile, rm, writeFile } from "node:fs/promises";
import { dirname, extname, join, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const siteOrigin = "https://www.marcosbodi.com";
const personName = "Marcos Bod\u00ed";
const siteName = personName;
const defaultDescription = "Portfolio of Marcos Bodi, Valencia-based 3D Artist specialized in CGI, VFX and product visualization.";
const defaultSocialImagePath = "/assets/projects/dron/cover_optimized.jpg";
const generatedMarker = "<!-- Generated by portfolio admin. Do not edit manually. -->";
const videoExtensions = new Set([".mp4", ".mov", ".webm"]);

function escapeHtml(value = "") {
  return String(value)
    .replace(/&/g, "&amp;")
    .replace(/</g, "&lt;")
    .replace(/>/g, "&gt;")
    .replace(/"/g, "&quot;")
    .replace(/'/g, "&#39;");
}

function escapeScriptString(value = "") {
  return JSON.stringify(String(value));
}

function escapeJsonLd(value = "") {
  return JSON.stringify(value)
    .replace(/</g, "\\u003c")
    .replace(/>/g, "\\u003e")
    .replace(/&/g, "\\u0026");
}

function jsonLdScript(value) {
  return `<script type="application/ld+json">${escapeJsonLd(value)}</script>`;
}

function getFileStem(fileName = "") {
  return fileName.replace(/\.[^.]+$/, "");
}

function isVideoFile(fileName = "") {
  return videoExtensions.has(extname(fileName).toLowerCase());
}

function getPosterFileName(fileName = "") {
  return `${getFileStem(fileName)}_poster.jpg`;
}

function encodeUrlPath(path = "") {
  return path
    .split("/")
    .map((part) => part ? encodeURIComponent(part) : "")
    .join("/");
}

function absoluteUrl(path = "/") {
  const normalized = path.startsWith("/") ? path : `/${path}`;
  return `${siteOrigin}${encodeUrlPath(normalized)}`;
}

function cleanDescription(project = {}) {
  return project.description || project.summary || `${project.title || "Project"} by Marcos Bodi.`;
}

function getProjectImagePath(project = {}) {
  const coverFile = project?.media?.cover?.file || project?.media?.main?.file || "";
  const imageFile = isVideoFile(coverFile) ? getPosterFileName(coverFile) : coverFile;

  return imageFile
    ? `/assets/projects/${project.slug}/${imageFile}`
    : defaultSocialImagePath;
}

export function getProjectPagePath(project = {}) {
  return `/projects/${encodeURIComponent(project.slug)}.html`;
}

export function getProjectPageUrl(project = {}) {
  return absoluteUrl(getProjectPagePath(project));
}

export function getProjectSocialImageUrl(project = {}) {
  return absoluteUrl(getProjectImagePath(project));
}

